    private void combinedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combinedActionPerformed
        Object source = evt.getSource();

        //exit program
        if (source == exit){
            System.exit(0);
        } else if (source == clear1){
            //reset all selections
            buttonGroup1.clearSelection();
            jTextField1.setText("");
            jList1.clearSelection();
            jCheckBox1.setSelected(false);
        }

    }//GEN-LAST:event_combinedActionPerformed

