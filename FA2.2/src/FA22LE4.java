    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        //create order and replace variables

        //get selected salad
        salad = jList1.getSelectedValue();

        if (salad == "Chicken Salad"){
            saladPrice = cSalad;
        } else if (salad == "Tuna Salad"){
            saladPrice = tSalad;
        } else if (salad == "Vegetable Salad"){
            saladPrice = vSalad;
        } else {
            salad = "none";
            saladPrice = 0;
        }


        //get selected drink
        if (jRadioButton1.isSelected()){
            drink = jRadioButton1.getText();
            drinkPrice = soda;
        } else if (jRadioButton2.isSelected()){
            drink = jRadioButton2.getText();
            drinkPrice = juice;
        } else if (jRadioButton3.isSelected()){
            drink = jRadioButton3.getText();
            drinkPrice = milkTea;
        } else {
            drink = "none";
            drinkPrice = 0;
        }

        //get request
        request = jTextField1.getText();

        //get if water was selected
        if (jCheckBox1.isSelected()){
            water = "Water is requested";
        } else {
            water = "Water is not requested";
        }

        JFrame d = new JFrame();
        JOptionPane.showMessageDialog(d, "Order Created");
    }//GEN-LAST:event_jMenuItem2ActionPerformed

